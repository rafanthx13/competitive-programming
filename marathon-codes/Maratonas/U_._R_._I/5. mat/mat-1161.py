# Leia dois valores inteiros M e N indefinidamente. A cada leitura, 
# calcule e escreva a soma dos fatoriais de cada um dos valores lidos.
#  Utilize uma variável apropriada, pois cálculo pode resultar em um 
# valor com mais de 15 dígitos.

# Entrada
# O arquivo de entrada contém vários casos de teste. Cada caso contém dois 
# números inteiros M (0 ≤ M ≤ 20) e N (0 ≤ N ≤ 20). O fim da entrada é determinado por eof.

# Saída
# Para cada caso de teste de entrada, seu programa deve imprimir uma única linha,
#  contendo um número que é a soma de ambos os fatoriais (de M e N).

# Exemplos
# 4 4
# 0 0
# 0 2
# 1 2
# 5 0
# 20 20

# 48
# 2
# 3
# 3
# 121
# 4865804016353280000

h = {0: 1, 1 : 1, 20: 2432902008176640000}

from math import factorial

def fatt(x):
  if(x in h):
    return h[x]
  else:
    h[x] = factorial(x)
    return h[x]

while(True):
  try:
    a,b = list(map(int, input().split()))

    fat_a = fatt(a)
    fat_b = fatt(b)
    # print("a", fat_a, "||", "b", fat_b)
    print(fat_a + fat_b)

  except EOFError:
    break
  

